class Multiples{


    public static void main(String[] args) {
        System.out.print("Number: ");
        for (int i = 0; i <= 10; i++) {
            System.out.print(i);
        }

        System.out.print("Doubles: ");
        for (int i = 0; i <= 10; i++) {
            System.out.print(i * 2);
        }

        System.out.print("Triples: ");
        for (int i = 0; i <= 10; i++) {
            System.out.print(i * 3);
        }
    }
}